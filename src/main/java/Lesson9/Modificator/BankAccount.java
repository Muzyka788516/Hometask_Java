package Lesson9.Modificator;

public class BankAccount {

        String holderName;
        String holderSurname;
        double balance;
        String privateNumber;

    public BankAccount(String holderName, String holderSurname, double balance, String privateAccount) {
        this.holderName = holderName;
        this.holderSurname = holderSurname;
        this.balance = balance;
        this.privateNumber = privateAccount;
    }


}


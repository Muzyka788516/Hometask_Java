package Lesson4.CalculatorCode;

public class Calculator {

    double x;
    double y;

    public  double add(){
        double result =  x + y;
        return result;

        }

    public  double sub (){
        double result =  x - y;
        return result;

    }

    public  double mult (){
        double result =  x * y;
        return result;

    }

    public  double div (){
        double result =  x / y;
        return result;


    }

    }

